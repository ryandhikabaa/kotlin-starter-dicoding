package org.example.controlFlow

fun main (){
    var counter = 1
    println("Looping Ekspression While")
    while (counter <= 7){
        println("Hello, World!")
        counter++
    }

//    Do While
    counter = 1
    println("Looping Ekspression Do While")
    while (counter <= 7){
        println("Hello, World!")
        counter++
    }
}